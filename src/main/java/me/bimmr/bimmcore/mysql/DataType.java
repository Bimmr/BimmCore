package me.bimmr.bimmcore.mysql;

/**
 * The enum Data type.
 */
public enum DataType {
    /**
     * Int data type.
     */
    INT,
    /**
     * Char data type.
     */
    CHAR,
    /**
     * Varchar data type.
     */
    VARCHAR,
    /**
     * Tinyint data type.
     */
    TINYINT,
    /**
     * Smallint data type.
     */
    SMALLINT,
    /**
     * Boolean data type.
     */
    BOOLEAN,
    /**
     * Bigint data type.
     */
    BIGINT,
    /**
     * Float data type.
     */
    FLOAT,
    /**
     * Double data type.
     */
    DOUBLE;
}
